package loops;

public class WhileLoop {

	
	public static void main(String[] args) {
		

		//Syntax :
//		
//		while(boolean-condition)
//		{
//			actions will get perform once the condition becomes true
//		}
		
		int i =10;
		
		while(i<=20)
		{
			System.out.println("i is less than 20");
			
			i++;
			
		}
		

		
	}
}

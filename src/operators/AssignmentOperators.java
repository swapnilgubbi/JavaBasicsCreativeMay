package operators;

public class AssignmentOperators {

	public static void main(String[] args) {

		int a = 50;

		int b = 75;

		int c = 450;

		if (a % 2 == 0) {
			System.out.println("Number is Even ");
		}

		else {
			System.out.println("Number is odd ");
		}
		if (b % 2 == 0) {
			System.out.println("Number is Even ");
		} 
		else {
			System.out.println("Number is odd ");
		}

		if (c % 2 == 0) {
			System.out.println("Number is Even ");
		} 
		else {
			System.out.println("Number is odd");
		}
	}
}

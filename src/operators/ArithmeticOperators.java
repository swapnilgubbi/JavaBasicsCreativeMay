package operators;

public class ArithmeticOperators {

	public static void main(String[] args) {

		// addition : '+'

		int i = 20;

		int j = 10;

		int k = i + j;

		System.out.println(k);

		// Substraction :'-'

		int l = i - j;

		System.out.println(l);

		// Multiplication : '*'

		int m = i * j;

		System.out.println(m);

		// Division : '/'

		int n = i / j;

		System.out.println(n);

		// Modulus : '%'_ It always gives Remainder

		int p = 15;

		int q = 3;

		int r = p % q;

		System.out.println(r); // we get remainder as 0

		int s = 2;

		int t = p % s;

		System.out.println(t); // we get remainder as 1

	}

}
